router.put("/:email",async (req,res) => {
    const email = req.params.email;

    try {
        const updatedNewsletter = await Newsletter.findOneAndUpdate({email},
            {
                $set: req.body
            },
            { new: true }
        )
        res.status(200).json(updatedNewsletter)
    } catch (err) {
        res.status(500).json(err)
    }
})

